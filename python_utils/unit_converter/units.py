    # I changed up how this works so now any time a unit gets collected a
    # new instance of the unit is made. Tho the type checker inside of an IDE
    # is not able to work with units like km and mm and cm it no longer
    # shows that the attribute doesn't exist, this is better then showing
    # an error.
    def __getattribute__(self, item) -> Unit:
        if item.startswith('_'):
            return object.__getattribute__(self, item)

        if hasattr(UnitsModule, item):
            unit = getattr(UnitsModule, item)
            return unit()
        else:
            from .unit import Unit
            try:
                unit = Unit(item)
                return unit
            except ValueError as exception:
                raise AttributeError from exception


