class __UnitsModule(object):

    def __init__(self):
        mod = sys.modules[__name__]

        self.__name__ = mod.__name__
        self.__doc__ = mod.__doc__
        self.__package__ = mod.__package__
        self.__loader__ = mod.__loader__
        self.__spec__ = mod.__spec__
        self.__file__ = mod.__file__
        self.__original_module__ = mod
        # noinspection PyTypeChecker
        sys.modules[__name__] = self

    def __getattr__(self, item):
        if item in self.__dict__:
            return self.__dict__[item]

        # todo: This is not going to work.
        #  there is no method object.__getattr__
        #  that is the reason why there is a check
        #  for an attribute in __dict__
        # Don't handle special and hidden attributes
        elif item.startswith('_'):
            # noinspection PyUnresolvedReferences
            return super().__getattr__(item)

        elif hasattr(self.__original_module__, item):
            attr = getattr(self.__original_module__, item)
            return attr()

        else:
            from .unit import Unit
            try:
                return Unit(item)
            except ValueError as exception:
                raise AttributeError from exception

    def __setattr__(self, key, value):
        if key.startswith('__'):
            object.__setattr__(self, key, value)

        else:
            setattr(self.__original_module__, key, value)



